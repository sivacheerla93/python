# For string
a = input("Enter a number! ")  # It is string as it doesn't have int before the input
print("You've entered: ", a, " and type is: ", type(a))

b = input("Enter your name! ")
print("Your name is: ", b, "and type is: ", type(b))

# For integer
c = int(input("Enter any number: "))
print(c, type(c))

d = input("Enter another number: ")
d = int(d)
print(d, type(d))
