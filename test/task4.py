temp = float(input("Enter temperature: "))

if temp >= 40:
    print("Way too hot – stay inside!")
elif temp >= 30:
    print("Hot – beach time!")
elif temp >= 20:
    print("Lovely day – how about a picnic!?!")
elif temp >= 10:
    print("On the cold side – better take a jacket!")
elif temp < 10:
    print("Way too cold – stoke up the fire!")


